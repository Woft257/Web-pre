drop schema public cascade;
create schema public;

grant usage on schema public to anon, authenticated, service_role;
grant all on schema public to postgres, service_role;

create table public.contest_settings (
  id boolean primary key default true check (id),
  title text not null,
  home_team text not null default 'Argentina',
  away_team text not null default 'Spain',
  submission_closes_at timestamptz not null,
  predictions_open boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.invite_codes (
  id uuid primary key default gen_random_uuid(),
  code_hash text not null unique check (code_hash ~ '^[0-9a-f]{64}$'),
  code_hint text not null check (char_length(code_hint) = 4),
  status text not null default 'active' check (status in ('active', 'revoked')),
  created_at timestamptz not null default now(),
  claim_count integer not null default 0 check (claim_count >= 0),
  last_claimed_at timestamptz
);

create table public.event_users (
  id uuid primary key default gen_random_uuid(),
  uid text not null unique check (uid ~ '^[0-9]{8}$'),
  invite_code_id uuid not null references public.invite_codes(id) on delete restrict,
  status text not null default 'active' check (status in ('active', 'suspended')),
  auth_version integer not null default 1 check (auth_version > 0),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.predictions (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null unique references public.event_users(id) on delete restrict,
  winner text not null check (winner in ('argentina', 'spain')),
  argentina_score smallint not null check (argentina_score between 0 and 20),
  spain_score smallint not null check (spain_score between 0 and 20),
  messi_scores boolean not null,
  submitted_at timestamptz not null default clock_timestamp()
);

create index predictions_submitted_at_idx on public.predictions(submitted_at, id);

create table public.contest_results (
  id boolean primary key default true check (id),
  winner text not null check (winner in ('argentina', 'spain')),
  argentina_score smallint not null check (argentina_score between 0 and 20),
  spain_score smallint not null check (spain_score between 0 and 20),
  messi_scores boolean not null,
  is_published boolean not null default false,
  published_at timestamptz,
  updated_by text not null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  check ((not is_published and published_at is null) or (is_published and published_at is not null))
);

create table public.admin_audit_logs (
  id bigint generated by default as identity primary key,
  actor text not null,
  action text not null,
  detail jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

create table public.rate_limit_buckets (
  scope text not null,
  key_hash text not null,
  window_started_at timestamptz not null default now(),
  request_count integer not null default 0 check (request_count >= 0),
  updated_at timestamptz not null default now(),
  primary key (scope, key_hash)
);

create index rate_limit_buckets_updated_idx on public.rate_limit_buckets(updated_at);

