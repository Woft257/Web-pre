create or replace function public.consume_rate_limit(
  p_scope text,
  p_key_hash text,
  p_limit integer,
  p_window_seconds integer
)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
declare
  v_count integer;
  v_now timestamptz := now();
begin
  if p_limit <= 0 or p_window_seconds <= 0 then
    raise exception 'INVALID_RATE_LIMIT_CONFIG';
  end if;

  insert into public.rate_limit_buckets (
    scope, key_hash, window_started_at, request_count, updated_at
  ) values (
    p_scope, p_key_hash, v_now, 1, v_now
  )
  on conflict (scope, key_hash) do update set
    request_count = case
      when rate_limit_buckets.window_started_at <= v_now - make_interval(secs => p_window_seconds)
      then 1 else rate_limit_buckets.request_count + 1
    end,
    window_started_at = case
      when rate_limit_buckets.window_started_at <= v_now - make_interval(secs => p_window_seconds)
      then v_now else rate_limit_buckets.window_started_at
    end,
    updated_at = v_now
  returning request_count into v_count;

  delete from public.rate_limit_buckets
  where updated_at < v_now - interval '2 days';

  return v_count <= p_limit;
end;
$$;

create or replace function public.claim_contest_access(
  p_uid text,
  p_code_hash text
)
returns public.event_users
language plpgsql
security definer
set search_path = public
as $$
declare
  v_code public.invite_codes%rowtype;
  v_user public.event_users%rowtype;
begin
  if p_uid is null or p_uid !~ '^[0-9]{8}$' then
    raise exception 'INVALID_UID';
  end if;
  if p_code_hash is null or p_code_hash !~ '^[0-9a-f]{64}$' then
    raise exception 'INVALID_INVITE_CODE';
  end if;

  select * into v_code
  from public.invite_codes
  where code_hash = p_code_hash
  for update;

  if not found or v_code.status <> 'active' then
    raise exception 'INVALID_INVITE_CODE';
  end if;

  select * into v_user
  from public.event_users
  where uid = p_uid;

  if found then
    if v_user.invite_code_id <> v_code.id or v_user.status <> 'active' then
      raise exception 'INVALID_ACCESS_PAIR';
    end if;
    return v_user;
  end if;

  begin
    insert into public.event_users(uid, invite_code_id)
    values (p_uid, v_code.id)
    returning * into v_user;
  exception
    when unique_violation then
      select * into v_user from public.event_users where uid = p_uid;
      if not found or v_user.invite_code_id <> v_code.id or v_user.status <> 'active' then
        raise exception 'INVALID_ACCESS_PAIR';
      end if;
      return v_user;
  end;

  update public.invite_codes
  set claim_count = claim_count + 1,
      last_claimed_at = now()
  where id = v_code.id;

  return v_user;
end;
$$;

create or replace function public.submit_contest_prediction(
  p_user_id uuid,
  p_winner text,
  p_argentina_score smallint,
  p_spain_score smallint,
  p_messi_scores boolean
)
returns public.predictions
language plpgsql
security definer
set search_path = public
as $$
declare
  v_settings public.contest_settings%rowtype;
  v_prediction public.predictions%rowtype;
begin
  select * into v_settings from public.contest_settings where id = true;
  if not found or not v_settings.predictions_open or now() >= v_settings.submission_closes_at then
    raise exception 'PREDICTIONS_CLOSED';
  end if;
  if not exists (
    select 1 from public.event_users
    where id = p_user_id and status = 'active'
  ) then
    raise exception 'USER_NOT_ACTIVE';
  end if;
  if p_winner not in ('argentina', 'spain') then
    raise exception 'INVALID_WINNER';
  end if;
  if p_argentina_score not between 0 and 20 or p_spain_score not between 0 and 20 then
    raise exception 'INVALID_SCORE';
  end if;

  insert into public.predictions(
    user_id, winner, argentina_score, spain_score, messi_scores
  ) values (
    p_user_id, p_winner, p_argentina_score, p_spain_score, p_messi_scores
  )
  returning * into v_prediction;

  return v_prediction;
exception
  when unique_violation then
    raise exception 'PREDICTION_ALREADY_SUBMITTED';
end;
$$;

create or replace function public.publish_contest_result(
  p_winner text,
  p_argentina_score smallint,
  p_spain_score smallint,
  p_messi_scores boolean,
  p_actor text
)
returns public.contest_results
language plpgsql
security definer
set search_path = public
as $$
declare
  v_result public.contest_results%rowtype;
begin
  if p_winner not in ('argentina', 'spain') then
    raise exception 'INVALID_WINNER';
  end if;
  if p_argentina_score not between 0 and 20 or p_spain_score not between 0 and 20 then
    raise exception 'INVALID_SCORE';
  end if;

  insert into public.contest_results(
    id, winner, argentina_score, spain_score, messi_scores,
    is_published, published_at, updated_by
  ) values (
    true, p_winner, p_argentina_score, p_spain_score, p_messi_scores,
    true, now(), p_actor
  )
  on conflict (id) do update set
    winner = excluded.winner,
    argentina_score = excluded.argentina_score,
    spain_score = excluded.spain_score,
    messi_scores = excluded.messi_scores,
    is_published = true,
    published_at = now(),
    updated_by = excluded.updated_by
  returning * into v_result;

  update public.contest_settings
  set predictions_open = false
  where id = true;

  insert into public.admin_audit_logs(actor, action, detail)
  values (
    p_actor,
    'publish_contest_result',
    jsonb_build_object(
      'winner', p_winner,
      'argentina_score', p_argentina_score,
      'spain_score', p_spain_score,
      'messi_scores', p_messi_scores
    )
  );

  return v_result;
end;
$$;

create or replace function public.set_predictions_open(
  p_open boolean,
  p_actor text
)
returns public.contest_settings
language plpgsql
security definer
set search_path = public
as $$
declare
  v_settings public.contest_settings%rowtype;
begin
  if p_open and exists (
    select 1 from public.contest_results where id = true and is_published
  ) then
    raise exception 'RESULT_ALREADY_PUBLISHED';
  end if;

  update public.contest_settings
  set predictions_open = p_open
  where id = true
  returning * into v_settings;

  insert into public.admin_audit_logs(actor, action, detail)
  values (p_actor, 'set_predictions_open', jsonb_build_object('open', p_open));

  return v_settings;
end;
$$;

create view public.contest_leaderboard
with (security_invoker = true)
as
select
  p.user_id,
  public.mask_uid(u.uid) as masked_uid,
  p.submitted_at,
  ((p.winner = r.winner)::integer * 10
    + ((p.argentina_score = r.argentina_score and p.spain_score = r.spain_score)::integer * 10)
    + ((p.messi_scores = r.messi_scores)::integer * 10)) as points,
  ((p.winner = r.winner)::integer
    + (p.argentina_score = r.argentina_score and p.spain_score = r.spain_score)::integer
    + (p.messi_scores = r.messi_scores)::integer) as correct_answers,
  row_number() over (
    order by
      ((p.winner = r.winner)::integer
        + (p.argentina_score = r.argentina_score and p.spain_score = r.spain_score)::integer
        + (p.messi_scores = r.messi_scores)::integer) desc,
      p.submitted_at asc,
      p.id asc
  ) as rank
from public.predictions p
join public.event_users u on u.id = p.user_id
join public.contest_results r on r.id = true and r.is_published = true
where u.status = 'active';

